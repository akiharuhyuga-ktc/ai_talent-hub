export function getMockResponse(userMessage: string, memberName?: string): string {
  for (const qa of MOCK_QA_PAIRS) {
    if (qa.keywords.some(kw => userMessage.includes(kw))) {
      return qa.response(memberName)
    }
  }
  return FALLBACK_RESPONSE(memberName)
}
